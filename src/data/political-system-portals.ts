export const politicalSystemNodeById = new Map(
  politicalSystemNodes.map((node) => [node.id, node]),
);

export const portalById = new Map(
  politicalSystemPortals.map((portal) => [portal.id, portal]),
);

export function portalPath(portal: PoliticalSystemPortal, locale: Locale) {
  return `/${locale}/${portal.slug[locale]}`;
}

export function findPortalBySlug(slug: string, locale: Locale) {
  return politicalSystemPortals.find((portal) => portal.slug[locale] === slug);
}

export function assertPoliticalSystemPortalData() {
  if (politicalSystemNodeById.size !== politicalSystemNodes.length) {
    throw new Error("Political-system portal data contains duplicate node ids.");
  }

  politicalSystemPortals.forEach((portal) => {
    const root = politicalSystemNodeById.get(portal.rootNodeId);
    if (!root || root.category !== portal.id) {
      throw new Error(`Portal ${portal.id} has an invalid root node.`);
    }
  });

  politicalSystemNodes.forEach((node) => {
    if (node.parentId && !politicalSystemNodeById.has(node.parentId)) {
      throw new Error(`Node ${node.id} references a missing parent ${node.parentId}.`);
    }
    node.relationships.forEach((relationship) => {
      if (!politicalSystemNodeById.has(relationship.targetId)) {
        throw new Error(`Node ${node.id} references a missing relationship target ${relationship.targetId}.`);
      }
    });
  });
}
